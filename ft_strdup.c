#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*string;

	if (!s1)
		return (NULL);
	string = (char *)malloc((ft_strlen(s1) + 1) * sizeof(char));
	if (!string)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		string[i] = s1[i];
		i++;
	}
	string[i] = '\0';
	return (string);
}
